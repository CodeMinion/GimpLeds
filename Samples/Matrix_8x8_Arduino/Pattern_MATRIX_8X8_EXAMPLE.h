using namespace NS_MATRIX_8X8_EXAMPLE;

		
class Pattern_MATRIX_8X8_EXAMPLE : public GimpLedPattern 
{

  public:
    Pattern_MATRIX_8X8_EXAMPLE(Adafruit_NeoPixel& strip): GimpLedPattern(strip){}

    ~Pattern_MATRIX_8X8_EXAMPLE(){}

    void playPattern() 
    {
      int totalFrames = sizeof(MATRIX_8X8_EXAMPLE) / sizeof(uint32_t*);
      for (int framePos = 0; framePos < totalFrames; framePos ++)
      {
        int frameTotalLeds = pgm_read_dword(&(MATRIX_8X8_EXAMPLE_SIZES[framePos]));
		int ledOffset = 0;
        for (int ledPos = 0; ledPos < frameTotalLeds; ledPos++)
        {
          if(mInterrupt)
          {
            // If we are interrupted stop the pattern. "Clean" LED pattern.
            mStrip.clear();
            mStrip.show();
            mInterrupt = false;
            return;
          }
          uint32_t ledColor = pgm_read_dword(&(MATRIX_8X8_EXAMPLE[framePos][ledPos]));
          int blue = ledColor & 0x00FF;
          int green = (ledColor >> 8) & 0x00FF;
          int red = (ledColor >>  16) & 0x00FF;
          mStrip.setPixelColor(ledPos + ledOffset, red, green, blue);

        }
        mStrip.show();
        delay(MATRIX_8X8_EXAMPLE_DELAY);
      }
    }

    
    void stopPattern() 
    {
      mInterrupt = true;
    }
};
		
